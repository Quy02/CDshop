    private int getPriceBorrowDate(){
        int rowCount = bang1.getRowCount();
        if(rowCount > 0 ){
            int totalPrice = 0;
            for (int i = 0; i < rowCount; i++) {
                int price = Integer.parseInt(bang1.getValueAt(i, 4).toString()) * Integer.parseInt(bang1.getValueAt(i, 5).toString());
                totalPrice += price;
            }

            int ngayTra = txtngaytra1.getSelectedIndex();
            if(ngayTra==0 ){
                return totalPrice;
            }else if(ngayTra == 1){
                return ((totalPrice*25)/100)+totalPrice; 
            }else if(ngayTra ==2){
                return ((totalPrice*50)/100)+totalPrice;
            }
        }
        
        return 0;
    }
