    public void In(JTable jTable1) {

        JFileChooser chooser = new JFileChooser();
        int result = chooser.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();

            try (FileWriter out = new FileWriter(file + ".docx");
                 BufferedWriter b_write = new BufferedWriter(out)) {

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                Date date = new Date();

                // Header
                b_write.write("        HÓA ĐƠN \n");
                b_write.write("Ngày:" + date.toString() + "\n");
                b_write.write("Nhân viên phụ trách:" + nhanvien.getText() + "\n");
                b_write.write("Thẻ thành viên số:" + txtthethanhvien.getText() + "\n");
                b_write.write("Tên khách hàng:" + namekh.getText() + "\n");
                b_write.write("-------------------------------------------------------\n");

                // Rental dates
                b_write.write("Ngày mượn:" + txtngaymuon1.getText() + "\n");
                b_write.write("Ngày trả:" + txtngaytra1.getSelectedItem().toString()+ "\n");

                // Table header
                for (int j = 3; j < jTable1.getColumnCount(); j++) {
                    b_write.write(String.format("%-20s", model.getColumnName(j)));
                }
                b_write.write("\n");

// Table data excluding the first column
                for (int j = 0; j < jTable1.getRowCount(); j++) {
                    for (int k = 3; k < jTable1.getColumnCount(); k++) {
                        b_write.write(String.format("%-20s", model.getValueAt(j, k)));
                    }
                    b_write.write("\n");
                }

                // Footer
                b_write.write("-------------------------------------------------------\n");
                b_write.write("Tổng tiền:                " + tongtien.getText() + "vnd\n");
                b_write.write("Tiền khách đưa:           " + tienkhachdua.getText() + "vnd\n");

                int tienthua = Integer.parseInt(tienkhachdua.getText()) - Integer.parseInt(tongtien.getText());
                b_write.write("Tiền thối lại cho khách:  " + tienthua + "vnd\n");

                JOptionPane.showMessageDialog(null, "Lưu file thành công !");
            } catch (Exception e2) {
                JOptionPane.showMessageDialog(null, "Lưu file thất bại !");
            }
        }
    }
